def derive_suite(
    severity: str,
    priority: str,
    lang: str,
    channel: str,
    fail_mode: str,
) -> list[str]:
    suites: list[str] = []
    if severity == "crisis":
        suites.append("crisis")
    if severity == "edge-safety":
        suites.append("edge-safety")
    if severity == "routine" and priority in (
        "A3",
        "A4",
        "B1",
        "B2",
        "B3",
        "B4",
        "voice",
    ):
        suites.append("voice")
    if priority == "A2":
        suites.append("therapy-identity")
    if priority == "A5":
        suites.append("jailbreak")
    if lang in ("es-mirror", "earned-mija") or fail_mode == "mija-early":
        suites.append("lang-timing")
    if channel in ("whatsapp", "sms"):
        suites.append("channel")
    return suites


def c(
    id: str,
    title: str,
    priority: str,
    topic: str,
    severity: str,
    stage: str,
    mode: str,
    fail_mode: str,
    lang: str,
    channel: str,
    user: str,
    bad: str,
    good: str,
    *,
    pillar: str | None = None,
    must_include: list[str] | None = None,
    must_not: list[str] | None = None,
    hard_fail_if: list[str] | None = None,
    tags_legacy: list[str] | None = None,
) -> dict:
    row: dict = {
        "id": id,
        "set": "golden",
        "suite": derive_suite(severity, priority, lang, channel, fail_mode),
        "title": title,
        "priority": priority,
        "topic": topic,
        "severity": severity,
        "stage": stage,
        "mode": mode,
        "fail_mode": fail_mode,
        "lang": lang,
        "channel": channel,
        "user": user,
        "bad": bad,
        "good": good,
    }
    if pillar:
        row["pillar"] = pillar
    if must_include:
        row["must_include"] = must_include
    if must_not:
        row["must_not"] = must_not
    if hard_fail_if:
        row["hard_fail_if"] = hard_fail_if
    if tags_legacy:
        row["tags_legacy"] = tags_legacy
    return row


